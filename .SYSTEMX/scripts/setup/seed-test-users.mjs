#!/usr/bin/env node

import { existsSync, mkdirSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import process from 'node:process'

const root = process.cwd()
const writeMode = process.argv.includes('--write')
const envPath = path.join(root, '.env.test')

const TEST_ACCOUNTS = [
  { email: 'Test-SU@example.test', level: 5, role: 'owner' },
  { email: 'Test-Admin@example.test', level: 4, role: 'admin' },
  { email: 'Test-Pro@example.test', level: 3, role: 'diamond' },
  { email: 'Test-User_Paid@example.test', level: 2, role: 'pro' },
  { email: 'Test-User_Free@example.test', level: 1, role: 'member' },
  { email: 'Test-User_Public@example.test', level: 0, role: 'public' },
]

const domain = process.env.WSG_TEST_EMAIL_DOMAIN || 'example.test'
const password = process.env.WSG_TEST_PASSWORD || 'WSG-Test-Local-Only-000!'

function projectEmail(email) {
  return email.replace('example.test', domain)
}

const rows = TEST_ACCOUNTS.map((account) => ({
  ...account,
  email: projectEmail(account.email),
  password,
  loginState: account.level === 0 ? 'public' : 'logged-in',
}))

console.log('WSG test account manifest')
console.log(`Mode: ${writeMode ? 'write local manifest' : 'dry-run'}`)
console.log(`Domain: ${domain}`)
console.log('')

for (const row of rows) {
  console.log(`- ${row.email} | Level ${row.level} | ${row.role} | ${row.loginState}`)
}

console.log('')
console.log('Firebase write behavior:')
console.log('- This template script does not create live users by default.')
console.log('- Use Firebase emulators first, then adapt the manifest to Admin SDK or REST setup.')
console.log('- Never commit real test passwords or production service-account JSON.')

if (writeMode) {
  const content = [
    '# Generated by .SYSTEMX/scripts/setup/seed-test-users.mjs',
    '# Local-only test fixture metadata. This file is git-ignored.',
    `WSG_TEST_EMAIL_DOMAIN=${domain}`,
    '',
    ...rows.flatMap((row) => {
      const prefix = `TEST_LEVEL_${row.level}`
      return [
        `${prefix}_EMAIL=${row.email}`,
        `${prefix}_PASSWORD=${row.level === 0 ? '' : row.password}`,
        `${prefix}_ROLE=${row.role}`,
      ]
    }),
    '',
  ].join('\n')

  if (!existsSync(path.dirname(envPath))) mkdirSync(path.dirname(envPath), { recursive: true })
  writeFileSync(envPath, content)
  console.log(`Wrote ${envPath}`)
}
